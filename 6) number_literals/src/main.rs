fn main() {
    let binary_number = 0b10;
    let binary_number_ASCII = b'A';
    let octal_number = 0o10;
    let decimal_number = 10;
    let hexadecimal_number = 0x10;
    
    println!("Binary Number is {}" , binary_number);
    println!("Binary Number from ASCII is {}" , binary_number_ASCII);
    println!("Octal Number is {}" , octal_number);
    println!("Decimal Number is {}" , decimal_number);
    println!("Hexadecimal Number is {}" , hexadecimal_number);
}
