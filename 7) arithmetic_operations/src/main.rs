fn main() {
    let x = 10.0;
    let y = 5.0;

    let add = x + y;
    let sub = x - y;
    let multiply = x * y;
    let divide = x / y;
    let modulus = x % y;

    println!("Addition is {}" , add);
    println!("Subtraction is {}" , sub);
    println!("Multiplicaton is {}" , multiply);
    println!("Division is {}" , divide);
    println!("Modulus or Remainder is {}" , modulus);
}
